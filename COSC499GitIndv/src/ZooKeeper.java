public class ZooKeeper {
	private int age;
    private String name;
    private String[] speciesKeeper;
    

    public ZooKeeper(int age, String name, String[] speciesKeeper){
        this.age = age;
        this.name = name;
        this.speciesKeeper = speciesKeeper; 
    }
    
    public int getAge(){
        return age;
    }
    public String getName(){
        return name;
    }

    public String getName(){
        return name;
    }
    public String[] getSpeciesKeeper(){
        return speciesKeeper;
    }

}
